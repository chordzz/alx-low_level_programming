#include "3-calc.h"

/**
 * op_add - returns the sum of integers
 * @a: first integer
 * @b: second integer
 * 
 * Return: integer - sum of a and b
 */
int op_add(int a, int b)
{
	return (a+b);
}


/**
 * op_sub - returns the difference between integers
 * @a: first integer
 * @b: second integer
 * 
 * Return: integer - difference between a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - returns the product of integers
 * @a: first integer
 * @b: second integer
 * 
 * Return: integer - product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - returns the division of integers
 * @a: first integer
 * @b: second integer
 * 
 * Return: integer - division of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
